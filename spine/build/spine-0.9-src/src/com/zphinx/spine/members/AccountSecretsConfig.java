package com.zphinx.spine.members;

/**
 * AccountSecretsConfig contains static constants used to configure the account secrets object during user activities 
 * 
 * @author David Ladapo
 * @version $1.0
 *          <p>
 *          Created: Aug 31, 2006 1:43:22 PM<br>
 *          Copyright &copy;Zphinx Software Solutions
 *          </p>
 */
public interface AccountSecretsConfig {

    /**
     * A int representing an adminstrative process saving this account secrets
     */
    public static final int ADMIN_SAVE = 1;

    /**
     * A int representing an activation process handling this account secrets
     */
    public static final int ACTIVATION = 2;

    /**
     * A int representing a lostpassword process reseting this account secrets, initiated by an administrator
     */
    public static final int ADMIN_RESET_LOSTPASSWORD = 3;

    /**
     * A int representing an adminstrator reseting a user's account secrets
     */
    public static final int ADMIN_RESET_ADMIN = 4;

    /**
     * A int representing a registration process saving this account secrets
     */
    public static final int ADMIN_REGISTRATION = 5;
    
    /**
     * A int representing a lostpassword process reseting this account secrets
     */
    public static final int RESET_LOSTPASSWORD = 3;


}